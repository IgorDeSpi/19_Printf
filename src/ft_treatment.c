#include "ft_printf.h"

int	is_type_list(int c)
{
	return ((c == 'c') || (c == 's') || (c == 'p') || (c == 'd') \
	|| (c == 'i') || (c == 'u') || (c == 'x') || (c == 'X') || (c == '%'));
}
